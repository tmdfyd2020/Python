## 숫자의 총합 구하기 ##
# 사용자로부터 다음과 같은 숫자를 입력받아 숫자의 총합을 구하는 프로그램을 작성하시오.
# 단, 숫자는 콤마로 구분하여 입력한다.
# 65,45,2,3,45,8

str = input("더할 숫자들을 입력하세요\n")

numbers = str.split(",")
print(numbers)

total = 0
for n in numbers:
    total += int(n)
print(total)
 
